#include "libft.h"

void	ft_putnbr(int nb)
{
	char	digit;

	if (nb == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb = -nb;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else
		{
			digit = nb + '0';
			ft_putchar(digit);
		}
	}
}
